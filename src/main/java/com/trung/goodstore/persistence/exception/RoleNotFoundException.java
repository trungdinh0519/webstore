package com.trung.goodstore.persistence.exception;

public class RoleNotFoundException extends RuntimeException {
	public static final long serialVersionUID = 123432432423423L;

	public RoleNotFoundException() {
		super();

	}

	public RoleNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public RoleNotFoundException(String arg0) {
		super(arg0);

	}

	public RoleNotFoundException(Throwable arg0) {
		super(arg0);

	}

}
